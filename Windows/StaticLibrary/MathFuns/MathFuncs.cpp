#include"MathFuncs.h"
namespace Math
{

	int MyMathFuns::add(int num1, int num2)
	{
		return num1 + num2;
	}
	int MyMathFuns::sub(int num1, int num2)
	{
		return num1 - num2;
	}
	int MyMathFuns::mul(int num1, int num2)
	{
		return num1*num2;
	}
	int MyMathFuns::div(int num1, int num2)
	{
		return num1 / num2;
	}
}